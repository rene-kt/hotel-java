    public void listarHosp() {
        DefaultTableModel tabela = (DefaultTableModel) jTable4.getModel();
        HospedeDAO hDAO = new HospedeDAO();

        for (Hospede h : hDAO.listar()) {
            SimpleDateFormat sdf = new SimpleDateFormat("HH:mm:ss");

            tabela.addRow(new Object[]{
                h.getId(),
                h.getNome(),
                h.getEmail(),
                h.getCpf(),
                h.getTelefone(),
                h.getLogin(),
                h.getSenha()

            });
        }
    }

    public void excluir() {
        Connection con = ConnectionFactory.getConnection();
        PreparedStatement stmt = null;
        try {
            stmt = con.prepareStatement("DELETE from tabelaclientes where id = ?");
            int setar = jTable4.getSelectedRow();
            Object valor = jTable4.getModel().getValueAt(setar, 0).toString();
            stmt.setString(1, (String) valor);
            int add = stmt.executeUpdate();

            if (add > 0) {
                JOptionPane.showMessageDialog(null, "H贸spede removido com sucesso");
                MenuGerente tg = new MenuGerente();
                tg.setVisible(true);
                dispose();

            }

        } catch (Exception e) {
            System.out.println(e);
        }

    }

    public void setar_texto() {
        PreparedStatement stmt = null;
        ResultSet rs = null;
        Connection con = ConnectionFactory.getConnection();

        try {
            stmt = con.prepareStatement("select nome, email, cpf, telefone, login, senha from tabelaclientes where id = ?");

            int setar = jTable4.getSelectedRow();
            Object valor = jTable4.getModel().getValueAt(setar, 0).toString();
            stmt.setString(1, (String) valor);

            rs = stmt.executeQuery();

            if (rs.next()) {

                jTextField1.setText(rs.getString("nome"));
                jTextField9.setText(rs.getString("email"));
                jTextField11.setText(rs.getString("cpf"));
                jTextField14.setText(rs.getString("telefone"));
                jTextField15.setText(rs.getString("login"));
                jTextField16.setText(rs.getString("senha"));

            }

        } catch (Exception e) {
            System.out.println(e);
        } finally {
            ConnectionFactory.closeConnection(con, stmt, rs);
        }

    }

    public void editar() {
        Hospede h = new Hospede();
        HospedeDAO hdao = new HospedeDAO();

        h.setNome(jTextField1.getText());
        h.setEmail(jTextField9.getText());
        h.setCpf(jTextField11.getText());
        h.setTelefone(jTextField14.getText());
        h.setLogin(jTextField15.getText());
        h.setSenha(jTextField16.getText());

        int setar = jTable4.getSelectedRow();
        Object valor = jTable4.getModel().getValueAt(setar, 0);
        h.setId((int) valor);
        hdao.editar(h);
        MenuGerente r = new MenuGerente();
        r.setVisible(true);
        dispose();
    }

