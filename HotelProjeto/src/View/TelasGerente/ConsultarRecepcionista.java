    public void listarHosp() {
        DefaultTableModel tabela = (DefaultTableModel) jTable4.getModel();
        RecepcionistaDAO rdao = new RecepcionistaDAO();

        for (Recepcionista h : rdao.listar()) {

            tabela.addRow(new Object[]{
                h.getNome(),
                h.getEmail(),
                h.getCpf(),
                h.getEndereco(),
                h.getLogin(),
                h.getHorario(),
                h.getSenha(),});
        }

    }

    public void pesquisar_hospede() {

        try {
            Connection con = ConnectionFactory.getConnection();
            PreparedStatement stmt = null;
            ResultSet rs = null;
            stmt = con.prepareStatement("select nome, email, cpf, endereco, login, horario senha from tabelaclientes where nome like ? and cpf like ? and perfil like 'Recepcionista' ");
            stmt.setString(1, txtCampoNome.getText() + "%");
            stmt.setString(2, txtCpf.getText() + "%");
            rs = stmt.executeQuery();
            jTable4.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception e) {
            System.out.println(e);
        }
    }

