    public void listarQuartos() {
        DefaultTableModel tabela = (DefaultTableModel) jTable1.getModel();
        QuartoDAO qDAO = new QuartoDAO();

        for (Quarto h : qDAO.listarDisp()) {
            tabela.addRow(new Object[]{
                h.getId(),
                h.getNumQuarto(),
                h.getPreco(),
                h.getAndar(),
                h.getDisponivel(),
                h.getHorario(),
            });

        }

    }

    

