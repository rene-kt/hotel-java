    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        HospedeDAO hDao = new HospedeDAO();
        Hospede h = new Hospede();
        Quarto q = new Quarto();
        QuartoDAO qdao = new QuartoDAO();

        int setarHosp = jTable4.getSelectedRow();
        Object valor = jTable4.getModel().getValueAt(setarHosp, 0);

        int setarQuarto = jTable1.getSelectedRow();
        Object valor2 = jTable1.getModel().getValueAt(setarQuarto, 0);

        int setarHor = jTable1.getSelectedRow();
        Object valor3 = jTable1.getModel().getValueAt(setarHor, 5);

        h.setId((int) valor);
        h.setIdquarto((int) valor2);
        h.setHorario((String) valor3);

        q.setId((int) valor2);
        q.setDisponivel("Indisponível");
        q.setAcumulador(1);

        hDao.reservarQuarto(h);
        qdao.reservado(q);
        dispose();
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton4ActionPerformed

