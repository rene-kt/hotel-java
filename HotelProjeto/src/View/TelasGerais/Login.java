    public void logar() {
        Hospede h = new Hospede();
        Connection con = ConnectionFactory.getConnection();
        PreparedStatement stmt = null;
        ResultSet rs = null;
        
        try {
            stmt = con.prepareStatement("select * from tabelaclientes where login = ? and senha = ?");
            Login l = new Login();
            
            stmt.setString(1, jTextField1.getText());
            stmt.setString(2, jPasswordField1.getText());
            
            rs = stmt.executeQuery();
            
            if (rs.next()) {
                HospedeDAO hdao = new HospedeDAO();
                
                String perfil = rs.getString(8);
                Hospede.setIdaux(rs.getInt(1));
                
                
                if (perfil.equals("Recepcionista")) {
                    MenuRecepcionista tl = new MenuRecepcionista();
                    tl.setVisible(true);
                    dispose();
                    
                } else if (perfil.equals("Gerente")) {
                    MenuGerente tg = new MenuGerente();
                    tg.setVisible(true);
                    dispose();
                } else if (perfil.equals("HÃ³spede")) {
                    MenuHospede mh = new MenuHospede();
                    mh.setVisible(true);
                    dispose();
                }else if (perfil.equals("adm")){
                    MenuADM ma = new MenuADM();
                    ma.setVisible(true);
                    dispose();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Senha ou login incorretos");
                
            }
            
        } catch (Exception e) {
            
            System.out.println(e);
            
        }
        
    }

